<div>  
<span style='font-weight: bold; font-size: 14px;'>HUMANS</span>
<br />
<br />  
  Objective: Survive the game, and complete missions in order to ensure your survival.
<br />
<br />
<OL>  
<LI><span style='color: red; font-weight: bold'>(NEW)</span> Humans must wear a bandana around THEIR UPPER ARM to indicate they are playing. This should be worn at all times when you are outside/taggable.
<LI>Make sure you are carrying your CORRECT ID card with your number at all times. You will hand this over to the zombie in the event you are tagged. If there is a conflict, call us at the HvZ Help Line (see below) but realize we may decide to flip and coin to decide your fate or immediately decide to zombify you.
<LI>In the event you are tagged, you must wait 10 minutes before you are allowed to tag anyone as a zombie. Move your armband to your neck (ie, stunned zombie) for 10 minutes. Then you may move it to your forehead and proceed with tagging 30 seconds after your headband is around your head (see ZOMBIES section below).
<LI>If you have not been marked as zombie within THREE (3) HOURS of being tagged, contact the HvZ Help Line (see below). You will remain a human, and will get a new ID. Note, any kills you got during those 3 hours STILL COUNT and their IDs should be given to the moderator when you call. 
<LI>Getting tagged on your backpack is NOT a valid tag.
<LI>Getting tagged on your weapon is NOT a valid tag.
<LI>Humans may not be tagged while inside safe zones (see SAFE ZONES below)
<LI><span style='color: red; font-weight: bold'>(NEW)</span> Humans may shoot and stun zombies from both safe and non-safe zones. The stun time is the same.
<LI>Humans may be tagged while off campus (such as walking around downtown)! However you are safe inside buildings off campus as usual. Please be respectful of others and their property when off campus.
</OL>
<br />
