<div>
<span style='font-weight: bold; font-size: 14px;'>ZOMBIES</span>
<br />
<br />
  Objective: Tag and devour as many humans as possible. Your goal is complete and utter zombification. Prevent humans from succeeding on missions.
<br />
<br />
<UL>
<LI>  
  HEADBANDS:
</UL>
<OL>  
<LI>A zombie must wear a bandana around their heads (ninja-style) for the duration of the game. Excluding when they are at work/practice.
<LI><span style='color: red; font-weight: bold'>(NEW)</span> Headbands must be worn around your forehead FOR AT LEAST 30 SECONDS before you may actually tag a human! (Such as moving it back after you are unstunned, or after putting it on for the first time). Tags may be invalidated if this rule is not followed.
<LI><span style='color: red; font-weight: bold'>(NEW)</span> Zombies may not use techniques to disguise their bandana. It must be clearly visible from a 50+ feet away. This means, do not hide your bandana under your hair, in a banana suit, or otherwise. Moderators reserve the right to invalidate tags if we feel your bandana was not clear enough!
<LI>IMPORTANT, the Original Zombie(s) do not need to wear a bandana around their head for the first 24 hours of the game.  The Original Zombie(s) will have a moderator signed index card that indicates they are the Original Zombie.  After the 24 hours the original zombie must wear the bandana around their head.
</OL>
<br />
<UL>
<LI>  
  BEING STUNNED:
</UL>
<OL>
<LI>When shot with a Nerf gun or hit with a sock/nerf dart (throwing Nerf darts is allowed), a zombie is stunned for 10 minutes. A stunned zombie may not interact with the game in any way.
<LI>Zombies are considered stunned if they maliciously pick up ammunition of humans. You may pick up darts to compile or throw them back to humans HOWEVER if a human requests you give him/her a dart which belongs to them, and you do not comply, you are considered stunned. So, best case is to just leave ammo on the ground.
<LI><span style='color: red; font-weight: bold'>(NEW)</span> Note, during missions, zombies will have respawn points instead of stun timers. You will need to visit a respawn point to become unstunned. This includes being stunned while the human is inside a safe zone.
<LI>When stunned, do NOT shield other zombies, follow humans, communicate with fellow zombies, etc. Move 20 feet away from the nearest player to ensure this. Moderators may remove you from the game if you do not follow this rule.
<LI>When stunned, move your headband around your neck to indicate you are stunned. Move it back after you are unstunned.
<LI>Head shots count! You are stunned if hit in the head by a Nerf Dart/Sock.
<LI>If a zombie swats/catches socks/darts, they are stunned.
</OL>
<br />
<UL>
<LI>  
  TAGGING HUMANS:
</UL>
<OL>
<LI><span style='color: red; font-weight: bold'>(NEW)</span> When a zombie tags a human, collect their ID Card. If there is dispute over a rule, you may use the HvZ Help Line for clarification. Otherwise, please work it out amongst yourselves.
<LI><span style='color: red; font-weight: bold'>(NEW)</span></span> A zombie may only tag TWO humans at a time (ie, one for each hand). During a mission, you may tag again after you collect the ID cards from the zombies (make sure to register the kills on the website within 3 hours!). When missions are not happening, zombies may only carry TWO ID cards at any given time (you need to register them on the website before getting more kills!). Call the HvZ Help Line (see below) if you are not near a computer.
<LI>Zombies have THREE (3) HOURS to report kills through the website. If you cannot make it to a computer, call the HvZ Help Line (see below) and we can report the kill for you.
<LI>If you do not report a kill within 3 hours, the player will remain human.
<LI>Zombies must get a tag (feed) every 48 hours to remain alive! This is regulated by the online system. If you become deceased, any tags you get after the fact will be invalidated.
</OL>
