<div>
<span style='font-weight: bold; font-size: 14px;'>SAFE ZONES</span>
<br />
<br />
  Safe zones are places and scenarios where humans may not be tagged and turned into a zombie. They include:
<br />
<br />
<OL>
<LI>Inside any building (res hall, rec center, library, dining hall, etc).
<LI>Inside any building off campus.
<LI>Attending any class (including outdoor classes, class ends when the instructor dismisses).
<LI>When a human has two feet inside any safe zone (even if they are leaning outside of the safe zone, see below SPECIAL NOTE AROUND DOORS)
<LI><span style='color: red; font-weight: bold'>(NEW)</span> Inside parking garages and inside cars during the day. (Note: parking garages are off limits during missions)
</OL>
<br />
<UL>
<LI>
  SPECIAL NOTE AROUND DOORS:
</UL>
<OL>
<LI> 
  Doors have been a considerable problem in the past. As a human, you are safe for 5 seconds once you touch a door handle. This allows you to get through the door safely. If you are not inside within 5 seconds, you may be tagged. You may not "re touch" the door handle to gain another 5 seconds.
<LI><span style='color: red; font-weight: bold'>(NEW)</span> You must have BOTH feet inside the building to be considered safe. If any part of your body touches the ground outside the building, you may be tagged until both feet are grounded again inside the building. 
<LI>As a human, when coming out of the building, you are safe for 5 seconds or 5 feet, whichever comes first.
<LI>Zombies may not enter buildings during missions.
</OL>
<br />
<UL>
<LI>
  SPECIAL NOTE AROUND PARKING GARAGES:
</UL>
<OL>
<LI>
  As noted, parking garages are safe zones during the day (consider them a building) and off limits during missions for security reasons. Parking garages don't have well defined "exits", but imagine an imaginary plane across the openings. If any part of your body touches the ground outside the plane, you may be tagged.
</OL>
